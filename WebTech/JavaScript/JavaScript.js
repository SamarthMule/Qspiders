
// JavaScript code to print "Hello Pune !!!" in different ways
// 1. Using console.log() : This will print the message in the console
console.log("Hello Pune !!!");

// 2. Using document.write() : This will print the message in the HTML document
document.write("Hello Pune !!!" + "<br>");
document.write("Hello Pune !!!" + "<br>");

// 3. Using document.writeln() : This will print the message in the HTML document with a line break
document.writeln("<h1>Hello Pune !!!</h1>");
document.writeln("<h1>Hello Pune !!!</h1>");

// 4. Using alert() : This will show a pop-up alert box with the message 
alert("Program to print Hello Pune !!!");

// 5. Using confirm() : This will show a pop-up confirmation box with the message
k = confirm("Program to print Hello Pune !!!");
document.write("You entered: " + k + "<br>");

// 6. Using prompt() : This will show a pop-up prompt box with the message
i = prompt("Program to print Hello Pune !!!");
document.write("You entered: " + i + "<br>");

// 7. Using innerHTML : This will print the message in the HTML document using innerHTML property
document.getElementById("demo").innerHTML = "This is innerHTML example" + "<br>";
